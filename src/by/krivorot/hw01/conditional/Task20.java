package by.krivorot.hw01.conditional;

public class Task20 {

	public static void main(String[] args) {
		// Определить, делителем каких чисел а, b, с является число k.

		int a = 2;
		int b = 47;
		int c = 34;
		int k = 2;

		if (a % k == 0) {
			System.out.println(+a);
		}
		if (b % k == 0) {
			System.out.println(+b);
		}
		if (c % k == 0) {
			System.out.println(+c);
		}
	}

}
